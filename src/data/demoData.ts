export const generateAttendanceData = (employeeIds: number[], month: number, year: number) => {
  const attendanceData:any = [];
  let id = 100;
  const daysInMonth = new Date(year, month + 1, 0).getDate();
  
  for (let day = 1; day <= daysInMonth; day++) {
    // Skip weekends for simplicity (you can remove this if you want weekends too)
    const date = new Date(year, month, day);
    if (date.getDay() === 0 || date.getDay() === 6) continue;
    
    const dateStr = `${year}-${String(month + 1).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
    
    // For each day, generate attendance for a subset of employees
    employeeIds.forEach(empId => {
      // Randomize attendance status
      const statusRandom = Math.random();
      let status = 'present';
      if (statusRandom < 0.05) status = 'absent';
      else if (statusRandom < 0.15) status = 'late';
      else if (statusRandom < 0.18) status = 'half-day';
      else if (statusRandom < 0.22) status = 'on-leave';
      
      const loginTime = status === 'absent' || status === 'on-leave' ? null : 
                       status === 'late' ? `${Math.floor(Math.random() * 30) + 9}:${Math.floor(Math.random() * 60)}` : 
                       '09:00';
      
      const logoutTime = status === 'absent' || status === 'on-leave' ? null : '18:00';
      
      const hours = status === 'absent' || status === 'on-leave' ? '-' : 
                   status === 'half-day' ? '4h 00m' : 
                   status === 'late' ? '8h 30m' : '9h 00m';
      
      attendanceData.push({
        id: id++,
        empId: empId,
        empName: `Employee ${empId}`,
        date: dateStr,
        loginTime,
        logoutTime,
        status,
        hours
      });
    });
  }
  
  return attendanceData;
};